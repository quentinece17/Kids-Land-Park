    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if ( evt.getSource() == jButton1 )
        {
            if (jTextField1.getText().equals("") || jPasswordField2.getText().equals("") || jPasswordField3.getText().equals(""))
            {
                FieldException ex = new FieldException();
                controller.setFieldExceptionLabel(ex.getMessage());
                controller.AffichageFieldsException();
            }

            if (!jPasswordField2.getText().equals(jPasswordField3.getText())){
                MatchingException ex = new MatchingException(" Passwords are not matching ");
                controller.setMatchingGuestExceptionLabel(ex.getMessage()); // Une fenetre de warning pop-up ici
                controller.setSavedData(s1Name, sFName, userAge);
                controller.AffichageMatchingGuest();
            }
            
            else
            {
                // Deja vérifier mais c'est pour etre sur 
                if (jPasswordField2.getText().equals(jPasswordField3.getText()))
                {
                    controller.createMember_inSQL(userFullName, userAge, jTextField1.getText(), jPasswordField2.getText(), image);
                    controller.AffichageCustomer();
                    dispose();  // La fenetre actuel disparait
                }
            }
        }// hello here
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

